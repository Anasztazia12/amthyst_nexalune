document.querySelectorAll('[data-carousel]').forEach((carousel) => {
    const slides = carousel.querySelectorAll('.carousel-slide');
    const dots = carousel.querySelectorAll('.carousel-dot');
    const prevButton = carousel.querySelector('.carousel-prev');
    const nextButton = carousel.querySelector('.carousel-next');
    let dragged = false;

    slides.forEach((slide, slideIndex) => {
        slide.addEventListener('click', () => {
            if (dragged) return;
            openLightbox(Array.from(slides), slideIndex);
        });
    });

    if (slides.length < 2) return;

    const AUTOPLAY_DELAY = 4500;
    let activeIndex = 0;
    let autoplayTimer = null;

    const showSlide = (index) => {
        activeIndex = (index + slides.length) % slides.length;
        slides.forEach((slide, i) => slide.classList.toggle('active', i === activeIndex));
        dots.forEach((dot, i) => dot.classList.toggle('active', i === activeIndex));
    };

    const stopAutoplay = () => {
        clearInterval(autoplayTimer);
        autoplayTimer = null;
    };

    const startAutoplay = () => {
        stopAutoplay();
        autoplayTimer = setInterval(() => showSlide(activeIndex + 1), AUTOPLAY_DELAY);
    };

    prevButton?.addEventListener('click', (event) => {
        event.stopPropagation();
        showSlide(activeIndex - 1);
        startAutoplay();
    });

    nextButton?.addEventListener('click', (event) => {
        event.stopPropagation();
        showSlide(activeIndex + 1);
        startAutoplay();
    });

    dots.forEach((dot, i) => {
        dot.addEventListener('click', (event) => {
            event.stopPropagation();
            showSlide(i);
            startAutoplay();
        });
    });

    carousel.addEventListener('mouseenter', stopAutoplay);
    carousel.addEventListener('mouseleave', startAutoplay);

    const SWIPE_THRESHOLD = 40;
    let touchStartX = 0;
    let touchDeltaX = 0;

    carousel.addEventListener('touchstart', (event) => {
        touchStartX = event.touches[0].clientX;
        touchDeltaX = 0;
        dragged = false;
        stopAutoplay();
    }, { passive: true });

    carousel.addEventListener('touchmove', (event) => {
        touchDeltaX = event.touches[0].clientX - touchStartX;
        if (Math.abs(touchDeltaX) > 10) dragged = true;
    }, { passive: true });

    carousel.addEventListener('touchend', () => {
        if (touchDeltaX > SWIPE_THRESHOLD) {
            showSlide(activeIndex - 1);
        } else if (touchDeltaX < -SWIPE_THRESHOLD) {
            showSlide(activeIndex + 1);
        }
        startAutoplay();
    });

    startAutoplay();
});

const wireWeb3Form = (form, statusEl, onSuccess) => {
    if (!form || !statusEl) return;
    const submitButton = form.querySelector('button[type="submit"]');

    form.addEventListener('submit', async (event) => {
        event.preventDefault();
        const dictionary = translations[currentLanguage] || translations.en;

        if (form.querySelector('.h-captcha')) {
            const captchaField = form.querySelector('textarea[name="h-captcha-response"]');
            if (!captchaField || !captchaField.value) {
                statusEl.textContent = dictionary.contact_status_captcha;
                statusEl.style.color = '#f87171';
                return;
            }
        }

        if (submitButton) submitButton.disabled = true;
        statusEl.textContent = dictionary.contact_status_sending;
        statusEl.style.color = '';

        try {
            const response = await fetch(form.action, {
                method: 'POST',
                headers: { Accept: 'application/json' },
                body: new FormData(form),
            });
            const result = await response.json();

            if (response.ok && result.success) {
                form.dataset.submitted = 'true';
                statusEl.textContent = dictionary.contact_status_success;
                statusEl.style.color = '#5eead4';
                form.reset();
                if (onSuccess) onSuccess();
            } else {
                statusEl.textContent = dictionary.contact_status_error;
                statusEl.style.color = '#f87171';
            }
        } catch (error) {
            statusEl.textContent = dictionary.contact_status_error;
            statusEl.style.color = '#f87171';
        } finally {
            if (submitButton) submitButton.disabled = false;
        }
    });
};

